# 基本运算符
print("1 + 2 = ", 1 + 2)
print("5 - 1 = ", 5 - 1)
print("2 * 7 = ", 2 * 7)
print("9 / 2 = ", 9 / 2)
print("11 // 5 = ", 11 // 5)     # 整除
print("11 % 5 = ", 11 % 5)       # 取余
print("2 ** 4 = ", 2 ** 4)       # 指数

# 复合赋值运算符
num = 10
num += 1
print("num += 1 = ", num)
num -= 3
print("num -= 3 = ", num)
num *= 2
print("num *= 2 = ", num)
num //= 3
print("num //= 3 = ", num)
num /= 2
print("num /= 2 = ", num)
num %= 3
print("num %= 3 = ", num)
num **= 2
print("num **= 2 = ", num)